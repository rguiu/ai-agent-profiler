#!/usr/bin/env node
// Collect benchmark baselines from tagged profiler sessions and write a
// markdown report that feeds the `Baseline:` slots in docs/aish-requirements.md.
//
// For each task it runs `aap compare --task <task> --json`, groups the sessions
// by agent, keeps only successful runs for mutating tasks (meta.verify === "pass"),
// and reports the mean of each metric across runs (distributions, not single points).
//
// Usage:
//   node benchmarks/baselines.mjs [task ...] [--out <file>] [--verified-only]
//   (default tasks: explain locate fix-bug add-feature; default out: benchmarks/BASELINES.md)
//
// Prereq: sessions have been captured, `aap parse` has run, and `aap` is on PATH.

import { execFileSync } from "node:child_process";
import { writeFileSync } from "node:fs";

const DEFAULT_TASKS = ["explain", "locate", "fix-bug", "add-feature"];
// Tasks whose success is checked; only verify=pass runs count toward the baseline.
const MUTATING = new Set(["fix-bug", "add-feature"]);

function parseArgs(argv) {
  const tasks = [];
  let out = "benchmarks/BASELINES.md";
  let verifiedOnly = false;
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--out") out = argv[++i];
    else if (a === "--verified-only") verifiedOnly = true;
    else tasks.push(a);
  }
  return { tasks: tasks.length ? tasks : DEFAULT_TASKS, out, verifiedOnly };
}

function compare(task) {
  try {
    const json = execFileSync("aap", ["compare", "--task", task, "--json"], {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    });
    return JSON.parse(json);
  } catch {
    return []; // no sessions for this task (aap compare exits non-zero)
  }
}

const METRICS = [
  ["requests", (s) => s.requests, "#8 fewer round-trips"],
  ["inputTokens", (s) => s.inputTokens, "context size"],
  ["outputTokens", (s) => s.outputTokens, ""],
  ["cost", (s) => s.cost, ""],
  ["toolCalls", (s) => s.toolCalls, ""],
  ["distinctTools", (s) => s.distinctTools, ""],
  ["resultTokens", (s) => s.resultTokens, "#3 amplification"],
  ["toolsTokens", (s) => s.toolsTokens, "#5 duplication"],
  ["recommendations", (s) => s.recommendations, ""],
];

const mean = (xs) =>
  xs.length ? xs.reduce((a, b) => a + b, 0) / xs.length : 0;
const agentOf = (s) => s.client ?? s.meta?.agent ?? "unknown";
const fmt = (key, v) =>
  key === "cost"
    ? `$${v.toFixed(4)}`
    : Math.round(v)
        .toString()
        .replace(/\B(?=(\d{3})+(?!\d))/g, ",");

function reportForTask(task, verifiedOnly) {
  const all = compare(task);
  const checked = MUTATING.has(task) || verifiedOnly;
  const byAgent = new Map();
  for (const s of all) {
    const agent = agentOf(s);
    const g = byAgent.get(agent) ?? { total: 0, passed: 0, kept: [] };
    g.total += 1;
    const pass = s.meta?.verify === "pass";
    if (pass) g.passed += 1;
    if (!checked || pass) g.kept.push(s);
    byAgent.set(agent, g);
  }
  return { task, checked, byAgent };
}

function renderTask({ task, checked, byAgent }) {
  const agents = [...byAgent.keys()].sort();
  if (agents.length === 0)
    return `### ${task}\n\n_No sessions captured yet._\n`;
  const head = ["Metric (mean/run)", ...agents];
  const lines = [`### ${task}`, ""];
  lines.push(`| ${head.join(" | ")} |`);
  lines.push(
    `| ${head.map((_, i) => (i === 0 ? "---" : "---:")).join(" | ")} |`,
  );
  const runsRow = agents.map((a) => {
    const g = byAgent.get(a);
    return checked ? `${g.kept.length} (of ${g.total})` : String(g.total);
  });
  lines.push(`| runs${checked ? " verified" : ""} | ${runsRow.join(" | ")} |`);
  if (checked) {
    const pass = agents.map((a) => {
      const g = byAgent.get(a);
      return g.total ? `${Math.round((g.passed / g.total) * 100)}%` : "—";
    });
    lines.push(`| verify pass-rate | ${pass.join(" | ")} |`);
  }
  for (const [key, get] of METRICS) {
    const cells = agents.map((a) =>
      fmt(key, mean(byAgent.get(a).kept.map(get))),
    );
    lines.push(`| ${key} | ${cells.join(" | ")} |`);
  }
  lines.push("");
  return lines.join("\n");
}

const { tasks, out, verifiedOnly } = parseArgs(process.argv.slice(2));
const reports = tasks.map((t) => reportForTask(t, verifiedOnly));

const header = [
  "# Benchmark baselines",
  "",
  "_Generated by `node benchmarks/baselines.mjs` — do not edit by hand._",
  "",
  "Per task, the mean of each metric across captured runs, grouped by agent. For",
  "mutating tasks (fix-bug/add-feature) only `verify=pass` runs are counted, so a",
  "baseline never rewards a run that failed the task. These numbers feed the",
  "`Baseline:` slots in [`docs/aish-requirements.md`](../docs/aish-requirements.md);",
  "the metric→capability mapping is noted inline there.",
  "",
  `Tasks: ${tasks.join(", ")}`,
  "",
];
const body = reports.map(renderTask).join("\n");
const md = header.join("\n") + body + "\n";

writeFileSync(out, md);
process.stdout.write(md);
process.stderr.write(`\nwrote ${out}\n`);
